split_string = "This string has been\nsplit over\nseveral\nlines"  # \n - new line character
print(split_string)

tabbed_string = "1\t2\t3\t4\t5"
print(tabbed_string)

print('The pet shop owner said "No, no, \'e\'s uh,... he\'s resting".')
# or
print("The pet shop owner said \"No, no, 'e's uh,... he's resting\".")

print("""The pet shop owner said "No, no, \
'e's uh,... he's resting". """)
# use \ to escape the line
another_split_string = """This string has been \
split over \
several \
lines"""

print(another_split_string)
